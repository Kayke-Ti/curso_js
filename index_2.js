/* Operadores e Operações */

// Declaração de variáveis
let n1 = 10; // Número inteiro
let n2 = 20; // Número inteiro
let n3 = "2"; // Texto (string)

// Exibição das variáveis na página
document.write("<h1>N1: " + n1 + "</h1>");
document.write("<h1>N2: " + n2 + "</h1>");
document.write("<h1>N3: " + n3 + "</h1>");

/* OPERAÇÕES */

// Adição
console.log(n1 + n2);

// Subtração
console.log(n1 - n2);

// Multiplicação
console.log(n1 * n2);

// Divisão
console.log(n1 / n2);

// Resto ou módulo
console.log(n1 % n2);

/* OPERADORES */

// Igual (valor)
console.log(n2 == n3);

// Idêntico (valor e tipo)
console.log(n2 === n3);

// Diferente de (valor)
console.log(n2 != n3);

// Não idêntico (valor e tipo)
console.log(n2 !== n3);

/* OPERADORES RELACIONAIS */

// Maior que
console.log(n1 > n2);

// Menor que
console.log(n1 < n2);

// Maior ou igual a
console.log(n1 >= n2);

// Menor ou igual a
console.log(n1 <= n2);

/* OPERADORES LÓGICOS */

let a = true;
let b = false;

// E lógico (AND)
console.log(a && b);

// OU lógico (OR)
console.log(a || b);

// Negação lógica (NOT)
console.log(!a);
console.log(!b);
console.log(!b)
